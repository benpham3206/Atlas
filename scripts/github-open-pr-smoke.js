import assert from "node:assert/strict";
import { rmSync } from "node:fs";
import { tmpdir } from "node:os";
import { join } from "node:path";
import { createApiServer } from "../apps/api/src/server.js";
import { createFilePersistence } from "../apps/api/src/persistence.js";
import { createGitHubClientFromEnv, createGitHubPolicyFromEnv } from "../apps/api/src/github-client.js";
import { getAgentManifest } from "../apps/api/src/agent-gateway.js";

const DATA_FILE = join(tmpdir(), `atlas-github-open-pr-smoke-${process.pid}.json`);
const DEFAULT_REPOSITORY = "benpham3206/Atlas";
const DEFAULT_BASE_BRANCH = "main";

function startServer() {
  const persistence = createFilePersistence(DATA_FILE);
  const githubClient = createGitHubClientFromEnv();
  const envPolicy = createGitHubPolicyFromEnv();
  const githubPolicy = {
    allowed_repositories:
      envPolicy.allowed_repositories.length > 0 ? envPolicy.allowed_repositories : repositoryAllowlist(),
    allowed_base_branches:
      envPolicy.allowed_base_branches.length > 0 ? envPolicy.allowed_base_branches : baseBranchAllowlist(),
    dry_run: envPolicy.dry_run || process.env.GITHUB_DRY_RUN === "1" || process.env.GITHUB_DRY_RUN === "true"
  };
  const server = createApiServer({ persistence, githubClient, githubPolicy });

  return new Promise((resolve) => {
    server.listen(0, "127.0.0.1", () => {
      const { port } = server.address();
      resolve({ server, baseUrl: `http://127.0.0.1:${port}` });
    });
  });
}

function stopServer(server) {
  return new Promise((resolve) => server.close(resolve));
}

async function api(baseUrl, method, path, { token, body } = {}) {
  const headers = { "content-type": "application/json" };

  if (token) {
    headers.authorization = `Bearer ${token}`;
  }

  const response = await fetch(`${baseUrl}${path}`, {
    method,
    headers,
    body: body ? JSON.stringify(body) : undefined
  });

  const payload = await response.json().catch(() => ({}));
  return { status: response.status, payload };
}

function step(label) {
  console.log(`\n▶ ${label}`);
}

function ok(message) {
  console.log(`  ✓ ${message}`);
}

function repositoryAllowlist() {
  const raw = process.env.GITHUB_ALLOWED_REPOSITORIES ?? DEFAULT_REPOSITORY;
  return raw.split(",").map((item) => item.trim()).filter(Boolean);
}

function baseBranchAllowlist() {
  const raw = process.env.GITHUB_ALLOWED_BASE_BRANCHES ?? DEFAULT_BASE_BRANCH;
  return raw.split(",").map((item) => item.trim()).filter(Boolean);
}

async function main() {
  const manifest = getAgentManifest();
  const toolNames = manifest.tools.map((tool) => tool.name);
  assert.ok(toolNames.includes("github.open_pr"));
  assert.equal(toolNames.includes("github.merge_pr"), false);
  ok(`manifest exposes github.open_pr and no merge tool (${manifest.scopes.join(", ")})`);

  let { server, baseUrl } = await startServer();

  step("Seed workspace, GoalContract, and scoped delegation");
  const workspace = (await api(baseUrl, "POST", "/workspaces", { body: { name: "GitHub PR Smoke" } })).payload.data;
  const goalContract = (
    await api(baseUrl, "POST", `/workspaces/${workspace.id}/goal-contracts`, {
      body: {
        objective: "Prove github.open_pr boundary",
        allowed_actions: ["github.open_pr"],
        blocked_actions: ["github.merge_pr", "protected_branch_merge"],
        done_definition: "Allowlist, dry-run, and audit paths verified"
      }
    })
  ).payload.data;
  const agent = (await api(baseUrl, "POST", "/agents", { body: { display_name: "GitHub Smoke Agent" } })).payload.data;
  const delegation = (
    await api(baseUrl, "POST", `/workspaces/${workspace.id}/agent-delegations`, {
      body: {
        agent_id: agent.id,
        role: "editor",
        scopes: ["github.pr:create"],
        allowed_tools: ["github.open_pr"],
        goal_contract_id: goalContract.id,
        ttl_seconds: 3600
      }
    })
  ).payload.data;
  ok(`delegation ${delegation.id} bound to goal contract ${goalContract.id}`);

  step("Allowlist denial is structural (no GitHub call)");
  const denied = await api(baseUrl, "POST", "/agent/tools/github.open_pr", {
    token: delegation.id,
    body: {
      repository: "other/repo",
      title: "Blocked",
      body: "Should not reach GitHub.",
      head_branch: "codex/blocked",
      base_branch: "main"
    }
  });
  assert.equal(denied.status, 403);
  assert.equal(denied.payload.error, "github_repository_not_allowed");
  ok(`disallowed repository -> ${denied.status} ${denied.payload.error}`);

  step("Dry-run records artifact + audit without calling GitHub");
  const dryRun = await api(baseUrl, "POST", "/agent/tools/github.open_pr", {
    token: delegation.id,
    body: {
      repository: repositoryAllowlist()[0],
      title: "Atlas github.open_pr dry-run smoke",
      body: "Generated by scripts/github-open-pr-smoke.js",
      head_branch: "codex/github-open-pr-smoke",
      base_branch: baseBranchAllowlist()[0],
      dry_run: true
    }
  });
  assert.equal(dryRun.status, 200);
  assert.equal(dryRun.payload.data.result.state, "dry_run");
  ok(`dry-run artifact ${dryRun.payload.data.result.id} state=${dryRun.payload.data.result.state}`);

  const audit = await api(baseUrl, "GET", "/audit/verify");
  assert.equal(audit.payload.data.valid, true);
  const events = (await api(baseUrl, "GET", `/workspaces/${workspace.id}/audit-events`)).payload.data;
  const githubAttempts = events.filter((event) => event.event_type === "github.pull_request.open_attempted");
  assert.ok(githubAttempts.length >= 2);
  ok(`audit chain valid; ${githubAttempts.length} github.pull_request.open_attempted events`);

  const liveRequested = process.env.GITHUB_LIVE_SMOKE === "1" || process.env.GITHUB_LIVE_SMOKE === "true";

  if (!liveRequested) {
    ok("live GitHub call skipped (set GITHUB_LIVE_SMOKE=1 with GITHUB_TOKEN + GITHUB_HEAD_BRANCH to run it)");
    await stopServer(server);
    rmSync(DATA_FILE, { force: true });
    console.log("\n✅ GitHub open-PR boundary smoke complete (allowlist + dry-run + audit).\n");
    return;
  }

  if (!process.env.GITHUB_TOKEN) {
    throw new Error("GITHUB_LIVE_SMOKE=1 requires GITHUB_TOKEN");
  }

  const headBranch = process.env.GITHUB_HEAD_BRANCH;

  if (!headBranch) {
    throw new Error("GITHUB_LIVE_SMOKE=1 requires GITHUB_HEAD_BRANCH pointing at an existing remote branch");
  }

  step(`Live GitHub open_pr against ${repositoryAllowlist()[0]} (${headBranch} -> ${baseBranchAllowlist()[0]})`);
  await stopServer(server);
  process.env.GITHUB_DRY_RUN = "0";
  ({ server, baseUrl } = await startServer());

  const workspaceLive = (await api(baseUrl, "POST", "/workspaces", { body: { name: "GitHub PR Live Smoke" } })).payload.data;
  const goalContractLive = (
    await api(baseUrl, "POST", `/workspaces/${workspaceLive.id}/goal-contracts`, {
      body: {
        objective: "Live github.open_pr proof",
        allowed_actions: ["github.open_pr"],
        blocked_actions: ["github.merge_pr"],
        done_definition: "Draft PR opened on GitHub"
      }
    })
  ).payload.data;
  const agentLive = (await api(baseUrl, "POST", "/agents", { body: { display_name: "GitHub Live Agent" } })).payload.data;
  const delegationLive = (
    await api(baseUrl, "POST", `/workspaces/${workspaceLive.id}/agent-delegations`, {
      body: {
        agent_id: agentLive.id,
        role: "editor",
        scopes: ["github.pr:create"],
        allowed_tools: ["github.open_pr"],
        goal_contract_id: goalContractLive.id,
        ttl_seconds: 3600
      }
    })
  ).payload.data;

  const live = await api(baseUrl, "POST", "/agent/tools/github.open_pr", {
    token: delegationLive.id,
    body: {
      repository: repositoryAllowlist()[0],
      title: "Atlas github.open_pr live smoke",
      body: "Automated live boundary proof from scripts/github-open-pr-smoke.js",
      head_branch: headBranch,
      base_branch: baseBranchAllowlist()[0],
      draft: true
    }
  });

  if (live.status !== 200) {
    throw new Error(
      `Live github.open_pr failed (${live.status} ${live.payload.error ?? "unknown"}): ${live.payload.message ?? JSON.stringify(live.payload.details ?? [])}`
    );
  }

  assert.ok(live.payload.data.result.external_url?.startsWith("https://github.com/"));
  ok(`live draft PR ${live.payload.data.result.external_url}`);

  await stopServer(server);
  rmSync(DATA_FILE, { force: true });
  console.log("\n✅ GitHub open-PR boundary smoke complete (allowlist + dry-run + live + audit).\n");
}

main().catch((error) => {
  console.error("\n❌ GitHub open-PR smoke failed:");
  console.error(error);
  rmSync(DATA_FILE, { force: true });
  process.exit(1);
});
